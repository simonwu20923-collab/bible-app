function toBibleChaptersText(raw) {
  return raw.trim().split('\n')
    .map(line => { const m = line.match(/^\d+:(\d+)\s+(.*)/); return m ? `${m[1]} ${m[2]}` : null; })
    .filter(Boolean).join('\n');
}

// Convert "ch:v text" lines → "ABBR ch:v text\n..." (verses table format)
function toVersesText(raw, abbr) {
  return raw.trim().split('\n')
    .map(line => { const m = line.match(/^(\d+:\d+)\s+(.*)/); return m ? `${abbr} ${m[1]} ${m[2]}` : null; })
    .filter(Boolean).join('\n');
}

// Get only lines for a specific verse range (inclusive)
function getVerseRange(raw, fromVerse, toVerse) {
  return raw.trim().split('\n').filter(line => {
    const m = line.match(/^\d+:(\d+)/);
    if (!m) return false;
    const v = parseInt(m[1]);
    return v >= fromVerse && v <= toVerse;
  }).join('\n');
}

