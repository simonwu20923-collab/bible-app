async function upsertBibleChapters() {
  console.log('\n=== Upserting bible_chapters ===');
  for (const row of BIBLE_CHAPTERS_UPSERTS) {
    const verseCountZh = row.text_zh.split('\n').length;
    const verseCountSc = row.text_sc.split('\n').length;
    console.log(`  ${row.book_abbr} ch${row.chapter}: zh=${verseCountZh}v, sc=${verseCountSc}v`);
    if (DRY_RUN) continue;

    const { error } = await supabase
      .from('bible_chapters')
      .upsert({ book_abbr: row.book_abbr, chapter: row.chapter, text_zh: row.text_zh, text_sc: row.text_sc },
               { onConflict: 'book_abbr,chapter', ignoreDuplicates: false });
    if (error) throw new Error(`bible_chapters upsert failed for ${row.book_abbr} ${row.chapter}: ${error.message}`);
    console.log(`    ✓ upserted`);
  }
}

async function updateVerses() {
  console.log('\n=== Updating verses table ===');
  for (const upd of VERSES_UPDATES) {
    console.log(`  date=${upd.date} operation=${upd.operation}`);
    console.log(`    zh_add: ${upd.zh_add.split('\n').length} lines`);
    console.log(`    sc_add: ${upd.sc_add.split('\n').length} lines`);

    if (DRY_RUN) {
      console.log(`    [dry-run] first zh line: ${upd.zh_add.split('\n')[0]}`);
      continue;
    }

    // Fetch current row
    const { data, error: fetchErr } = await supabase
      .from('verses')
      .select('ot_text_zh,ot_text_sc')
      .eq('date', upd.date)
      .single();
    if (fetchErr) throw new Error(`fetch failed for ${upd.date}: ${fetchErr.message}`);

    let new_zh, new_sc;
    if (upd.operation === 'replace') {
      new_zh = upd.zh_add;
      new_sc = upd.sc_add;
    } else {
      const existing_zh = (data.ot_text_zh || '').trimEnd();
      const existing_sc = (data.ot_text_sc || '').trimEnd();
      new_zh = existing_zh ? existing_zh + '\n' + upd.zh_add : upd.zh_add;
      new_sc = existing_sc ? existing_sc + '\n' + upd.sc_add : upd.sc_add;
    }

    const { error: updateErr } = await supabase
      .from('verses')
      .update({ ot_text_zh: new_zh, ot_text_sc: new_sc })
      .eq('date', upd.date);
    if (updateErr) throw new Error(`update failed for ${upd.date}: ${updateErr.message}`);
    console.log(`    ✓ updated`);
  }
}

async function main() {
  if (DRY_RUN) console.log('*** DRY RUN MODE — no writes will be made ***\n');
  await upsertBibleChapters();
  await updateVerses();
  console.log('\nDone.');
}

main().catch(err => { console.error(err); process.exit(1); });
